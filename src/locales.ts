export function getBrowserLang(): LangCode {
  if (typeof navigator === "undefined") return "en";
  const userLang = navigator.language.slice(0, 2).toLowerCase();
  if (["en", "es", "fr", "de", "ja", "pt"].includes(userLang)) {
    return userLang as LangCode;
  }
  return "en";
}
